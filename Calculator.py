a = int(input("Enter the first Number: "))
b = int(input("Enter the Second Number: "))
c = str(input("Enter the operator: "))

if(c=='+'):
    print(a+b)
    
elif(c=='-'):
    print(a-b)
      
elif(c=='*'):
    print(a*b)
    
elif(c=='/'):
    print(a/b)
    
elif(c=='//'):
    print(a//b)
    
elif(c=='**'):
    print(a**b)
    
elif(c=='%'):
    print(a%b)
    
    
